package chap03.textbook.selfEx;

public class SelfEx01 {
	public static void main(String[] args) {
		int var1 = 1;
		int var2 = 3;
		int var3 = 2;
		
		
		int result = var1 + var2 * var3;
		System.out.println("연산의 결과는=" + result + "입니다");

		result = (var1 + var2) * var3;
		System.out.println("연산의 결과는=" + result + "입니다");
		
		

	}
}
